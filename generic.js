function add(a) {
    return a;
}
var result = add(2);
console.log(result); // Output: 2
var result2 = add('Hello');
console.log(result2); // Output: Hello
var reverseArray = function (array) {
    return array.reverse();
};
console.log(reverseArray([1, 2, 3, 4])); // Output: [4, 3, 2, 1]
console.log(reverseArray(['a', 'b', 'c', 'd'])); // Output: ['d', 'c', 'b', 'a']
